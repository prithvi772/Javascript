// Date object is used to work with dates.
let hi =new Date();
console.log("Today's date is:",hi);

console.log("Year:",hi.getFullYear());
console.log("Month:",hi.getMonth()); // out:Month 2 As month starts here from 0.
console.log("Day:",hi.getDay());
console.log("Hours:",hi.getHours());
console.log("Minutes:",hi.getMinutes());
console.log("Seconds:",hi.getSeconds());




